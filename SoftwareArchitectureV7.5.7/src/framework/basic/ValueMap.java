package framework.basic;

import container.Queue;

public class ValueMap {
	private String peer;
	private Queue queue;
	
	public String getPeer() {
		return peer;
	}
	public void setPeer(String peer) {
		this.peer = peer;
	}
	public Queue getQueue() {
		return queue;
	}
	public void setQueue(Queue queue) {
		this.queue = queue;
	}

}
